type I18nContextType = {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: keyof typeof translations.pt, vars?: Record<string, string | number>) => string;
};

const I18nContext = createContext<I18nContextType>({
  lang: 'pt',
  setLang: () => {},
  t: (k) => translations.pt[k] || String(k),
});

export const I18nProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [lang, setLangState] = useState<Lang>('pt');

  useEffect(() => {
    AsyncStorage.getItem(STORAGE_KEY).then((stored) => {
      if (stored && ['pt', 'en', 'fr', 'es'].includes(stored)) {
        setLangState(stored as Lang);
      }
    });
  }, []);

  const setLang = (l: Lang) => {
    setLangState(l);
    AsyncStorage.setItem(STORAGE_KEY, l).catch(() => {});
  };

  const t = (key: keyof typeof translations.pt, vars?: Record<string, string | number>) => {
    let str = (translations[lang] as any)[key] || (translations.pt as any)[key] || String(key);
    if (vars) {
      Object.keys(vars).forEach((k) => {
        str = str.replace(`{${k}}`, String(vars[k]));
      });
    }
    return str;
  };

  return <I18nContext.Provider value={{ lang, setLang, t }}>{children}</I18nContext.Provider>;
};

export const useI18n = () => useContext(I18nContext);
